#include "libft.h"

int		ft_size_int(int n)
{
	int a;
	int size;

	a = 1;
	size = 0;
	if (n < 0)
	{
		n = -n;
		a = 0;
	}
	while ((n /= 10) > 1)
		size++;
	if (a == 0)
		return (size + 2);
	return (size + 1);
}
