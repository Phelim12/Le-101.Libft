#include "libft.h"

void	ft_putnbr(int n)
{
	int a;

	a = 0;
	if (n == -2147483648)
	{
		ft_putchar('-');
		ft_putchar('2');
		ft_putnbr(147483648);
		a = 1;
	}
	if (n < 0 && a != 1)
	{
		ft_putchar('-');
		n = -n;
	}
	if (n >= 10 && a != 1)
	{
		ft_putnbr(n / 10);
		ft_putchar(n % 10 + '0');
	}
	if (n < 10 && a != 1)
		ft_putchar(n + '0');
}
