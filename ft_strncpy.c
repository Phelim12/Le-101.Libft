#include "libft.h"

char	*ft_strncpy(char *d, const char *s, size_t n)
{
	unsigned int a;

	a = 0;
	while (s[a] && a < n)
	{
		d[a] = s[a];
		a++;
	}
	while (a < n)
		d[a++] = '\0';
	return (d);
}
